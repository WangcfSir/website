public class ImageArticle {

    /**
     * 返回状态码 正常:0;
     */
    private String src;

    /**
     * 返回的message
     */
    private String title;

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public ImageArticle(String src, String title) {
        this.src = src;
        this.title = title;
    }
}
