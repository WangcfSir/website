public enum ArticleEnum {

    MALE(1,"男"),
    FEMALE(2,"女"),
    UNKNOWN(3,"未知");

    private Integer code;
    private String name;

    ArticleEnum(Integer code, String name) {
        this.code = code;
        this.name = name;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
